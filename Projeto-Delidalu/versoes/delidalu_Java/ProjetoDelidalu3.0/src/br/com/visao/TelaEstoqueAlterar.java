    private void jBalterarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jBalterarActionPerformed
        // TODO add your handling code here:
        try{
            
      Estoque es = new Estoque();
            ManterEstoque dao = new ManterEstoque();
      es.setId_produto(Integer.valueOf(jTidproduto.getText()));
      es.setTipo_estoque(jTtipoestoque.getText());
      es.setPreco_unidade(Double.valueOf(jTprecounidade.getText()));
      es.setQuantidade_estoque(Integer.valueOf(jTquantidadeestoque.getText()));
      dao.editarEstoque(es); 
     
      
      jTidproduto.setText("");
      jTtipoestoque.setText("");
      jTprecounidade.setText("");
      jTquantidadeestoque.setText("");
     
      jTidproduto.requestFocus();
      }catch(Exception e){
        JOptionPane.showMessageDialog(this,"Erro de : " + e.getMessage());
      }
        
    }//GEN-LAST:event_jBalterarActionPerformed

