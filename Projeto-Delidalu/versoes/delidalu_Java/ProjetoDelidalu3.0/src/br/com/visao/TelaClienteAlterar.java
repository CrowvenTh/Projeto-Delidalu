    private void jBalterarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jBalterarActionPerformed
        // TODO add your handling code here:
        try{
            
      Cliente c = new Cliente();
            ManterCliente dao = new ManterCliente();
      c.setId_cliente(Integer.valueOf(jTidcliente.getText()));
      c.setCpf(jTcpf.getText());
      c.setNome(jTnome.getText());
      c.setEndereco(jTendereco.getText());
      c.setEmail(jTemail.getText());
      c.setTelefone(jTtelefone.getText());
      dao.editarCliente(c); 
      
      jTcpf.setText("");
      jTnome.setText("");
      jTendereco.setText("");
      jTemail.setText("");
      jTtelefone.setText("");
      jTidcliente.setText("");
      jTidcliente.requestFocus();
      }catch(Exception e){
        JOptionPane.showMessageDialog(this,"Erro de : " + e.getMessage());
      }
    }//GEN-LAST:event_jBalterarActionPerformed

