    private void jBinserirActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jBinserirActionPerformed
        // TODO add your handling code here:
        ClientePedido cp = new ClientePedido();
        ManterClientePedido dao = new ManterClientePedido();
        
        cp.setId_cliente(Integer.valueOf(jTidCliente.getText()));
        cp.setId_produto(Integer.valueOf(jTidProduto.getText()));
        cp.setQuantidade_pedido(Integer.valueOf(jTqtdPedido.getText()));
        cp.setTipo_pedido(jTtpoPedido.getText());
        cp.setMes(Integer.valueOf(jTmes.getText()));
        cp.setAno(Integer.valueOf(jTano.getText()));
        
        try {
            dao.inserir(cp);

            jTidCliente.setText("");
            jTidProduto.setText("");
            jTqtdPedido.setText("");
            jTtpoPedido.setText("");
            jTmes.setText("");
            jTano.setText("");
                jTidCliente.requestFocus();
        } catch (Exception ex) {
            Logger.getLogger(TelaClienteInserir.class.getName()).log(Level.SEVERE, null, ex);
        }
    }//GEN-LAST:event_jBinserirActionPerformed

